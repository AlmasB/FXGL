package com.almasb.fxgl.input.virtual

/**
 *
 *
 * @author Almas Baimagambetov (almaslvl@gmail.com)
 */
enum class VirtualButton {

    // These are dpad controls
    LEFT, RIGHT, UP, DOWN,

    /**
     * Equivalent to PS square.
     */
    X,

    /**
     * Equivalent to PS triangle.
     */
    Y,

    /**
     * Equivalent to PS cross.
     */
    A,

    /**
     * Equivalent to PS circle.
     */
    B
}