package com.almasb.fxgl.ui

/**
 * FXGL font types.
 *
 * @author Almas Baimagambetov (almaslvl@gmail.com)
 */
enum class FontType {

    /**
     * Standard UI font.
     */
    UI,

    /**
     * Monotype font.
     */
    MONO,

    /**
     * Font used inside the game.
     */
    GAME,

    /**
     * Font used for long text.
     */
    TEXT
}